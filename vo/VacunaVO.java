public class VacunaVO {
    private String id;
    private String idBovino;
    private String fechaVacunacion;
    private String nombreVacuna;
    private String dosisvacuna;
    private String viaAdmivacuna;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdBovino() {
        return idBovino;
    }

    public void setIdBovino(String idBovino) {
        this.idBovino = idBovino;
    }

    public String getFechaVacunacion() {
        return fechaVacunacion;
    }

    public void setFechaVacunacion(String fechaVacunacion) {
        this.fechaVacunacion = fechaVacunacion;
    }

    public String getNombreVacuna() {
        return nombreVacuna;
    }

    public void setNombreVacuna(String nombreVacuna) {
        this.nombreVacuna = nombreVacuna;
    }

    public String getDosisvacuna() {
        return dosisvacuna;
    }

    public void setDosisvacuna(String dosisvacuna) {
        this.dosisvacuna = dosisvacuna;
    }

    public String getViaAdmivacuna() {
        return viaAdmivacuna;
    }

    public void setViaAdmivacuna(String viaAdmivacuna) {
        this.viaAdmivacuna = viaAdmivacuna;
    }
}
